package For_loop;

public class Star_pattern {
	public static void main(String[]args) {
		int i,j;
		//using for loop.
		for(i=1;i<6;i++){
			System.out.println();
			for (j=1;j<=i;j++) {
				System.out.print("*");
			}
			
			}
		
		
		
	}

}
